// 1. Aritmeticki operatori(Aritmetic operators);
// 1.1 + operator
console.log(4 + 5);
// 1.2 - operator
console.log(4 - 5);
// 1.3 * operator
console.log(4 * 5);
// 1.4 / operator
console.log(20 / 5);
// 1.5 ** operator
console.log(3 ** 2);
// 1.6 % operator
console.log(11 % 3);
// 1.7 ++ operator (Povecava vrednost za 1)
let x = 5;
x++; // x = x + 1
console.log(x);

let y = 11;
// prefiksni operator
console.log(++y);


// 1.8 -- operator (smanjuje vrednost za 1)
let z = 99;
z-- // z = z - 1
console.log(z)

let w = 65;
console.log(--w)



